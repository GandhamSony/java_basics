public class Jumping9 {
    public static void main(String[]args){


    /*Jumpindg statements
        1.break
        2.continue
        3.return
    */

    //break
    for(int i = 0; i < 10; i++){
        if(i==4){
            break;
        }
        System.out.println(i);
    }


    //continue 
    for(int j = 0; j < 10; j++){
        if(j == 5){ //i==5 skip current iteration
            continue;
        }
        System.out.println(j);
    }

    //return 

    }
    
}
